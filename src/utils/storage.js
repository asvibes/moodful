const MOODS_KEY = "customMoods";
const JOURNAL_KEY = (id) => "journal_" + id;
const IMAGE_KEY   = (id) => "image_"   + id;
const BG_KEY      = (id) => "bg_"      + id;
const STICKERS_KEY= (id) => "stickers_"+ id;
const PINS_KEY    = (id) => "pins_"    + id;
export const loadCustomMoods    = () => { try { return JSON.parse(localStorage.getItem(MOODS_KEY) || "[]"); } catch { return []; } };
export const saveCustomMoods    = (m) => localStorage.setItem(MOODS_KEY, JSON.stringify(m));
export const loadJournal        = (id) => { try { return JSON.parse(localStorage.getItem(JOURNAL_KEY(id)) || "[]"); } catch { return []; } };
export const saveJournal        = (id, e) => localStorage.setItem(JOURNAL_KEY(id), JSON.stringify(e));
export const addJournalEntry    = (id, text, x, y) => { const e = loadJournal(id); const n = { id: Date.now(), text, ts: new Date().toISOString(), x: x||100, y: y||100 }; const u = [n, ...e]; saveJournal(id, u); return u; };
export const updateEntryPos     = (id, eid, x, y) => { const u = loadJournal(id).map(e => e.id===eid?{...e,x,y}:e); saveJournal(id,u); return u; };
export const deleteJournalEntry = (id, eid) => { const u = loadJournal(id).filter((e) => e.id !== eid); saveJournal(id, u); return u; };
export const loadImage          = (id) => localStorage.getItem(IMAGE_KEY(id)) || null;
export const saveImage          = (id, b64) => localStorage.setItem(IMAGE_KEY(id), b64);
export const removeImage        = (id) => localStorage.removeItem(IMAGE_KEY(id));
export const loadBgColor        = (id) => localStorage.getItem(BG_KEY(id)) || null;
export const saveBgColor        = (id, color) => localStorage.setItem(BG_KEY(id), color);
export const loadStickers       = (id) => { try { return JSON.parse(localStorage.getItem(STICKERS_KEY(id)) || "[]"); } catch { return []; } };
export const saveStickers       = (id, s) => localStorage.setItem(STICKERS_KEY(id), JSON.stringify(s));
